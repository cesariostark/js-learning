/*
BUCLE WHILE

- Es un bucle indeterminado ya que no sabemos cuantas veces dará vuelta su ejecución-
- Su sintaxis se compone de una única parte.
- Condicion de salida.
    while(condición){

        //codigo a ejecutar.
    }

*/

let pass = "hola";

while(pass != 'hola'){

    pass = prompt('Introduzca su contraseña');
}

console.log('Fin del bucle'); 


